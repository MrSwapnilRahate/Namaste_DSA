/*
------------------------------- Time Complexcity & Space Complexcity --------------------

Time Compexcity -> efficency of algorithm in terms of speed, as input size grows

Big O notaion -> Representation of wroste case complexity

O(1) < O(logn) < O(n) < O(nlogn) < O(n^2) < O(2^n) < O(n!)

Constant time < Binary search < Linear search < Merg sort < Nested loop < Recursion < Brute force computation

O(1) - fix time(constant) sabse fast
O(logn) - Binary Search style (adhe adhe me katate hue search krna)
O(n) - Linear Search (input ke brabr time)
O(nlogn) - Merg Sort
O(n^2) - do Nested loop 
O(2^n) - Recursive explosion (bhot bda time)
O(n!) - Brute force comptutation 


Space Complexcity -> how much extra space an algo uses

O(1) - Fixed memory
O(n) - Depents on i/p size (input jitni memory)
*/

